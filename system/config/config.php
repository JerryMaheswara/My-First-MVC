<?php

namespace system\config;

class Config
{

	protected static $_data = array();
	/////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////
	public static function setVal($key, $value, $extra=false) 
	{
		if($extra == false)
			static::$_data[$key] = $value;
		else 
			static::$_data[$key][$value] = $extra;
	}
	/////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////
	public static function getVal($key, $extra=false) 
	{
		if($extra == false)
			return static::$_data[$key];
		else
			return static::$_data[$key][$extra];
	}
}
